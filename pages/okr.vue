<h1>OKRs (Objectives and Key Results): A goal-making framework</h1>
<p>Objectives: Things you can be proud to announce. "Our customer satisfaction has never been higher" "We've fostered creativity and innovation throughout the organization"</p>
<p>Key Results: Quantifyable goals -- once you've done these, you can confidently say the things above</p>
<p>Initiatives: Action plans for achieving key results</p>

<h2>Types of OKRs</h2>
<h3>Aspirational</h3>
<p>Push exisisting, known boundaries. Risk of not achieving, but motivation to reach further</p>
<h3>Committed</h3>
<p>Achievable and practical. Confidently devote efforts here.</p>
<h3>Learning</h3>
<p>Experimentation and exploration. Like Aspirational, but push <em>unknown</em> boundaries</p>


